Grade=int(input("Enter The Student's Grade: "))
if Grade >= 93:
	print ("A")
elif Grade >= 90:
	print ("A-")
elif Grade >= 87:
	print ("B+")
elif Grade >= 83:
	print ("B")
elif Grade >= 80:
	print ("B-")
elif Grade >= 77:
	print ("C+")
elif Grade >= 73:
	print ("C")
elif Grade >= 70:
	print ("C-")
elif Grade >= 67:
	print ("D+")
elif Grade >= 63:
	print ("D")
elif Grade >= 60:
	print ("D-")
else:
	print ("F")
